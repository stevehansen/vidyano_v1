namespace Microsoft.Phone.Controls
{
    /// <summary>
    /// The expansion mode of a ListPicker.
    /// </summary>
    public enum ExpansionMode
    {
        /// <summary>
        /// Allows all items to be visible on the page.
        /// </summary>
        ExpansionAllowed,

        /// <summary>
        /// Requires all items to be displayed in a full screen popup.
        /// </summary>
        FullScreenOnly
    }
}
